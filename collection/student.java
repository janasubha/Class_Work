package collection;

public class student {
int id;
String name;
String course;
public student(int id, String name, String course) {
	super();
	this.id = id;
	this.name = name;
	this.course = course;
}
public int getId() {
	return id;
}
public String getName() {
	return name;
}
public String getCourse() {
	return course;
}


}
