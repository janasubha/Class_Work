package collection;

public class Student1 {

	private int id;
	private String name;
	private int rollno;
	
	

	public Student1(int id, String name, int rollno) {
		super();
		this.id = id;
		this.name = name;
		this.rollno = rollno;
	}



	public int getId() {
		return id;
	}



	public String getName() {
		return name;
	}



	public int getRollno() {
		return rollno;
	}
	
}
